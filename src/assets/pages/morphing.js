// easing
function easeInOutCubic(t) {
  return t < 0.5 ? 4 * t * t * t : 1 - Math.pow(-2 * t + 2, 3) / 2;
}

export const morphing = {
  init() {},
  on() {
    const path = document.getElementById("A");

    const duration = 1200;

    let start = null;
    let index = 0;

    function animate(time) {
      if (!start) start = time;

      let t = (time - start) / duration;
      t = Math.min(t, 1);

      const eased = easeInOutCubic(t);

      const interpolator = INTERPOLATORS[index];

      path.setAttribute("d", interpolator(eased));

      if (t < 1) {
        requestAnimationFrame(animate);
      } else {
        // siguiente forma
        index = (index + 1) % INTERPOLATORS.length;
        start = null;
        requestAnimationFrame(animate);
      }
    }

    requestAnimationFrame(animate);
  },
  off() {},
};
